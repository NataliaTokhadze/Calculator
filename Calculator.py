first = int(input("Write first number: "))
second = int(input("Write second number: "))
action = input("Write what you want to do with this nimbers: ")
if action == "+":
    print(first + second)
elif action == "-":
    print(first - second)
elif action == "*":
    print(first * second)
elif action == "/":
    print(first / second)
elif action == "**":
    print(first ** second)
elif action == "%":
    print(first / second) * 100
else:
    print("Use only \"+\" , \"-\", \"*\" , \"/\" , \"**\" or \"%\" ")

